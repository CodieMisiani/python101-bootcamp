#Stores your age in a variable
my_age = 18
#If your age is below 13 → print “Child”
if my_age < 13 :
  print("Child")
#If your age is between 13 and 19 → print “Teenager”
elif my_age >= 13 and my_age <= 19 :
  print("Teenager")
#Else → print “Adult”
else :
  print("Adult")